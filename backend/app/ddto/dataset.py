"""
Unified Data Layer — dataset.py

This module is the single point of truth for all biometric signal data
flowing into the DDTO. It transparently blends real STEW data with
synthetic signals, with a clear priority order:

Priority 1: Real STEW EEG data (when available)
  - Used for the EEG band power features (theta, alpha, beta and ratios)
  - Grounds the predictions in actual human neuroscience data
  - Dataset: STEW, Lim et al. 2018, IEEE TNSRE

Priority 2: Synthetic signal simulation (always available)
  - Used for eye tracking, performance proxies (not in STEW)
  - Used as full fallback when STEW is not present
  - Generated by signals.py — realistic but not from real humans

The DDTO payload always reports which data source is active,
so the demo dashboard can show judges "REAL EEG DATA" vs "SYNTHETIC".

Blending strategy:
  When STEW is available, EEG features come from a real recording
  matched to the operator's current estimated load level.
  The synthetic engine still drives eye tracking and performance proxies,
  plus the overall load/fatigue trajectory.
  This gives us the best of both: real neural patterns + complete signal set.
"""

import time
import random
from typing import Dict, Optional

from app.ddto.stew_loader import stew_loader
from app.ddto.signals import generate_signal
from app.ddto.profiles import OperatorProfile


def get_signal(
    profile: OperatorProfile,
    plant_risk: str = "low",
    active_scenarios: Optional[list] = None,
    scenario_override: Optional[str] = None,
) -> Dict:
    """
    Get a complete biometric signal sample for one operator tick.

    If STEW data is available:
      - EEG features come from a real STEW recording window
        matched to the operator's current load level
      - Eye tracking and performance proxies from synthetic engine
      - Source reported as 'stew_real'

    If STEW data is not available:
      - All features from synthetic engine
      - Source reported as 'synthetic'
    """
    # Generate the full synthetic signal first
    # (drives load trajectory, eye metrics, performance proxies)
    synthetic = generate_signal(
        profile,
        plant_risk=plant_risk,
        active_scenarios=active_scenarios or [],
        scenario_override=scenario_override,
    )

    # Determine current cognitive load score for STEW matching
    context = synthetic.get("_context", {})
    total_load = context.get("total_load", 0.0)

    # Try to get a real STEW EEG sample
    stew_sample = None
    if stew_loader.available:
        stew_sample = stew_loader.get_sample_for_load_score(total_load)

    if stew_sample:
        # Blend: replace EEG features with real STEW data
        # Apply small operator-specific offset so each person looks distinct
        # (STEW is population-level; we personalise by shifting relative to baseline)
        b = profile.baseline
        theta_offset = b.theta_baseline - 0.38   # Deviation from population mean
        alpha_offset = b.alpha_baseline - 0.40
        beta_offset  = b.beta_baseline  - 0.28

        blended_theta = max(0.05, min(0.95, stew_sample["theta"] + theta_offset + random.gauss(0, 0.008)))
        blended_alpha = max(0.05, min(0.90, stew_sample["alpha"] + alpha_offset + random.gauss(0, 0.008)))
        blended_beta  = max(0.05, min(0.85, stew_sample["beta"]  + beta_offset  + random.gauss(0, 0.006)))

        theta_alpha_ratio = blended_theta / max(blended_alpha, 0.001)
        beta_alpha_ratio  = blended_beta  / max(blended_alpha, 0.001)
        engagement_index  = blended_beta  / max(blended_theta + blended_alpha, 0.001)

        synthetic["eeg"] = {
            "theta":              round(blended_theta, 4),
            "alpha":              round(blended_alpha, 4),
            "beta":               round(blended_beta,  4),
            "theta_alpha_ratio":  round(theta_alpha_ratio, 4),
            "beta_alpha_ratio":   round(beta_alpha_ratio,  4),
            "engagement_index":   round(engagement_index,  4),
        }
        synthetic["_data_source"] = "stew_real"
        synthetic["_stew_workload_label"] = stew_sample.get("workload_label", "unknown")
    else:
        synthetic["_data_source"] = "synthetic"

    return synthetic


def get_data_source_info() -> Dict:
    """
    Returns current data source status for the frontend to display.
    Judges can see whether real EEG data is active.
    """
    stats = stew_loader.stats
    if stats["available"]:
        return {
            "source": "stew_real",
            "label": "Real EEG Data",
            "detail": f"STEW Dataset — {stats['low_windows'] + stats['high_windows']} windows, {stats['subject_count']} subjects",
            "citation": "Lim et al. (2018). IEEE Trans. Neural Syst. Rehabil. Eng.",
            "color": "#00e5a0",
        }
    return {
        "source": "synthetic",
        "label": "Synthetic Signals",
        "detail": "Grounded in published EEG ranges (Choi et al. 2018, Pakarinen et al. 2018)",
        "citation": "Signal parameters from NewTron literature review",
        "color": "#3ab8ff",
    }
